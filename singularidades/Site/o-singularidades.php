<script type="text/javascript">
    $(document).ready(function(e){
        //Tabs Toggle
        var currentClass = 'item1';
        $('.tabs a').click(function(e){
            e.preventDefault();
            var linkClass = $(this).attr('class');
            if(currentClass != linkClass){
                $('.tabs a.' + currentClass).parent().removeClass('active');
                $(this).parent().addClass('active');
                $('.tabs-content .' + currentClass).stop().fadeOut(200);
                $('.tabs-content .' + linkClass).stop().delay(200).fadeIn(200);
                currentClass = linkClass;
            }
        });
        
        /*Gallery*/
        var imageList    = new Array();
        var currentImage = 0;
        $(".thumbs a").each(function(index, element) {
            imageList[ index ]         = new Object();
            imageList[ index ].id      = index;
            imageList[ index ].element = $(element);
            imageList[ index ].image   = $(element).attr("href");
            imageList[ index ].title   = $(element).attr("title");
        });
        imageList[ currentImage ].element.addClass("active");

        $(".thumbs a").click(function(e) {
            e.preventDefault();
            for( key in imageList ) if( $(this).attr("href") == imageList[key].image ) currentImage = imageList[key].id;
            changeImage();
        });

        function changeImage(){
            $(".large-image img").attr("src", imageList[ currentImage ].image);
            $(".large-image img").hide().fadeIn(300);
            $(".thumbs a").removeClass("active");
            imageList[ currentImage ].element.addClass("active");
            $(".large-image a").attr("href", imageList[ currentImage ].image);
        }
    });
</script>